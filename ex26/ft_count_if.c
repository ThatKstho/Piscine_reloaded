int		ft_count_if(char **tab, int (*f)(char*))
{
	int n;
	int elm;

	n = 0;
	elm = 0;
	while (tab[n])
	{
		if (f(tab[n]) == 1)
			elm++;
		n++;
	}
	return (elm);
}
